#Iterration Chellange
fruits = [
    'apple',
    'bananas',
    'grapes',
    'mangos',
    'nectarines',
    'pears',
]


#=======================Iterration Chellange Solution===================

print("I love pears")

for fruit in fruits:
    print(fruit)

print("=============================")

counter = 0
while fruits[counter]  != 'nectarines':
    print(fruits[counter])
    counter += 1